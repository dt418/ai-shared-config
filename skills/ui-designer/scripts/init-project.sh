#!/bin/bash

# Initialize a React + Vite + Tailwind CSS + shadcn/ui project
# Usage: bash scripts/init-project.sh <project-name>

set -e

if [ -z "$1" ]; then
  echo "Usage: bash scripts/init-project.sh <project-name>"
  exit 1
fi

PROJECT_NAME="$1"

echo "Creating project: $PROJECT_NAME"

# Create Vite + React + TypeScript project
npm create vite@latest "$PROJECT_NAME" -- --template react-ts
cd "$PROJECT_NAME"

# Install base dependencies
npm install

# Install Tailwind CSS and related dependencies
npm install -D tailwindcss@3.4.17 postcss autoprefixer @types/node tailwindcss-animate

# Install shadcn/ui utility dependencies
npm install class-variance-authority clsx tailwind-merge lucide-react react-router-dom

# --- postcss.config.js ---
cat > postcss.config.js << 'POSTCSS_EOF'
export default {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
}
POSTCSS_EOF

